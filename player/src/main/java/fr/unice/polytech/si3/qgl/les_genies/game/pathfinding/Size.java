package fr.unice.polytech.si3.qgl.les_genies.game.pathfinding;

public class Size {
    private int x;
    private int y;

    public Size(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
